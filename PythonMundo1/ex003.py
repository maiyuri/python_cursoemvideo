#Crie um programa que leia dois números e mostre a soma entre eles:
numero1 = int(input("Insira o primeiro número a ser somado: "))
numero2 = int(input("Insira o segundo número a ser somado: "))
soma = numero1 + numero2

print("O resultado da soma do número {} e {} é: {}".format(numero1, numero2, soma))
